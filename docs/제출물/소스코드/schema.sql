    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id            BIGINT NOT NULL,
    schedule_id         BIGINT NOT NULL,
    title               VARCHAR(100),
    closes_at           TIMESTAMP(6),
    status              VARCHAR(20) NOT NULL,
    created_by          BIGINT NOT NULL,
    winner_candidate_id BIGINT,
    created_at          TIMESTAMP(6) NOT NULL,
    updated_at          TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_vote_sessions_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_vote_sessions_schedule FOREIGN KEY (schedule_id) REFERENCES schedules (id),
    CONSTRAINT fk_vote_sessions_creator FOREIGN KEY (created_by) REFERENCES users (id),
    CONSTRAINT ck_vote_sessions_status CHECK (status IN ('OPEN', 'CLOSED'))
);

CREATE TABLE vote_candidates (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vote_session_id BIGINT NOT NULL,
    place_id        BIGINT NOT NULL,
    registered_by   BIGINT NOT NULL,
    memo            VARCHAR(500),
    created_at      TIMESTAMP(6) NOT NULL,
    updated_at      TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_vote_candidates_session FOREIGN KEY (vote_session_id) REFERENCES vote_sessions (id),
    CONSTRAINT fk_vote_candidates_place FOREIGN KEY (place_id) REFERENCES places (id),
    CONSTRAINT fk_vote_candidates_registrant FOREIGN KEY (registered_by) REFERENCES users (id)
);
CREATE INDEX idx_vote_candidates_session ON vote_candidates (vote_session_id);

CREATE TABLE votes (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    candidate_id BIGINT NOT NULL,
    voter_id     BIGINT NOT NULL,
    score        INTEGER NOT NULL,
    created_at   TIMESTAMP(6) NOT NULL,
    updated_at   TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_votes_candidate_voter UNIQUE (candidate_id, voter_id),
    CONSTRAINT fk_votes_candidate FOREIGN KEY (candidate_id) REFERENCES vote_candidates (id),
    CONSTRAINT fk_votes_voter FOREIGN KEY (voter_id) REFERENCES users (id),
    CONSTRAINT ck_votes_score CHECK (score BETWEEN 1 AND 5)
);

-- ===================== 비용 / 정산 =====================
CREATE TABLE expenses (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id           BIGINT NOT NULL,
    payer_id           BIGINT NOT NULL,
    created_by         BIGINT NOT NULL,
    amount             BIGINT NOT NULL,
    category           VARCHAR(20) NOT NULL,
    split_type         VARCHAR(20) NOT NULL,
    description        VARCHAR(255),
    memo               VARCHAR(255),
    paid_at            DATE NOT NULL,
    source_schedule_id BIGINT,                  -- 일정→정산 자동등록 멱등키
    deleted_at         TIMESTAMP(6),
    created_at         TIMESTAMP(6) NOT NULL,
    updated_at         TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_expenses_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_expenses_payer FOREIGN KEY (payer_id) REFERENCES users (id),
    CONSTRAINT fk_expenses_creator FOREIGN KEY (created_by) REFERENCES users (id),
    CONSTRAINT ck_expenses_amount CHECK (amount BETWEEN 1 AND 100000000),
    CONSTRAINT ck_expenses_category CHECK (category IN ('MEAL', 'LODGING', 'TRANSPORT', 'TICKET', 'OTHER')),
    CONSTRAINT ck_expenses_split_type CHECK (split_type IN ('EQUAL', 'RATIO', 'AMOUNT'))
);
CREATE INDEX idx_expenses_group_paid_at ON expenses (group_id, paid_at DESC, id DESC) WHERE deleted_at IS NULL;
CREATE INDEX idx_expenses_source_schedule ON expenses (source_schedule_id) WHERE source_schedule_id IS NOT NULL;

CREATE TABLE expense_splits (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    expense_id  BIGINT NOT NULL,
    user_id     BIGINT NOT NULL,
    owed_amount BIGINT NOT NULL,
    CONSTRAINT fk_expense_splits_expense FOREIGN KEY (expense_id) REFERENCES expenses (id),
    CONSTRAINT fk_expense_splits_user FOREIGN KEY (user_id) REFERENCES users (id),
    CONSTRAINT ck_expense_splits_owed_amount CHECK (owed_amount > 0)
);
CREATE INDEX idx_expense_splits_expense ON expense_splits (expense_id);

CREATE TABLE settlements (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id             BIGINT NOT NULL,
    from_user_id         BIGINT NOT NULL,
    to_user_id           BIGINT NOT NULL,
    amount               BIGINT NOT NULL,
    status               VARCHAR(20) NOT NULL,
    sender_confirmed_at  TIMESTAMP(6),
    receiver_confirmed_at TIMESTAMP(6),
    created_at           TIMESTAMP(6) NOT NULL,
    updated_at           TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_settlements_group_users UNIQUE (group_id, from_user_id, to_user_id),
    CONSTRAINT fk_settlements_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_settlements_from_user FOREIGN KEY (from_user_id) REFERENCES users (id),
    CONSTRAINT fk_settlements_to_user FOREIGN KEY (to_user_id) REFERENCES users (id),
    CONSTRAINT ck_settlements_amount CHECK (amount > 0),
    CONSTRAINT ck_settlements_distinct_users CHECK (from_user_id <> to_user_id),
    CONSTRAINT ck_settlements_status CHECK (status IN ('PENDING', 'SENT', 'COMPLETED'))
);
CREATE INDEX idx_settlements_group_status ON settlements (group_id, status);

-- ===================== 알림 / 추천 / 회고 =====================
CREATE TABLE notifications (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id     BIGINT NOT NULL,
    recipient_id BIGINT NOT NULL,
    type         VARCHAR(40) NOT NULL,
    message      VARCHAR(255) NOT NULL,
    target_path  VARCHAR(255) NOT NULL,
    read_at      TIMESTAMP(6),
    created_at   TIMESTAMP(6) NOT NULL,
    updated_at   TIMESTAMP(6) NOT NULL,
