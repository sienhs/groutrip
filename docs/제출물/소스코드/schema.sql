);

CREATE TABLE retrospectives (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id   BIGINT NOT NULL,
    user_id    BIGINT NOT NULL,
    content    VARCHAR(200) NOT NULL,
    rating     INTEGER NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_retrospectives_group_user UNIQUE (group_id, user_id),
    CONSTRAINT fk_retrospectives_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_retrospectives_user FOREIGN KEY (user_id) REFERENCES users (id),
    CONSTRAINT ck_retrospectives_rating CHECK (rating BETWEEN 1 AND 5)
);

