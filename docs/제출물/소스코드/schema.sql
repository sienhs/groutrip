-- ============================================================================
-- 그룹 여행 협업 플랫폼 (enjoy-trip) — DB Schema (PostgreSQL 16)
-- ----------------------------------------------------------------------------
-- 이 파일은 산출물 제출용 "통합 스키마(최종 상태)"이다.
-- 운영 원천(Source of Truth)은 Flyway 마이그레이션:
--   backend/src/main/resources/db/migration/V2 ~ V29
-- 아래 DDL은 모든 마이그레이션을 적용한 최종 형태를 한 파일로 정리한 것이다.
-- 초기 데이터(설문 문항·테스트 계정)는 애플리케이션 기동 시 DataInitializer가 시드한다.
-- ============================================================================

-- ===================== 인증 / 계정 =====================
CREATE TABLE users (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email               VARCHAR(255) NOT NULL UNIQUE,
    password            VARCHAR(255) NOT NULL,           -- 소셜 전용 전환 후 placeholder
    name                VARCHAR(255) NOT NULL,
    avatar_key          VARCHAR(255),                    -- S3 object key
    avatar_content_type VARCHAR(100),
    payout_link         VARCHAR(512),                    -- AES-GCM 암호문(Base64)
    payout_account      VARCHAR(512),                    -- AES-GCM 암호문(Base64)
    deleted_at          TIMESTAMP(6),                    -- 탈퇴 익명화 soft delete
    created_at          TIMESTAMP(6) NOT NULL,
    updated_at          TIMESTAMP(6) NOT NULL
);

CREATE TABLE oauth_identities (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id          BIGINT NOT NULL,
    provider         VARCHAR(20) NOT NULL,
    provider_user_id VARCHAR(255) NOT NULL,
    created_at       TIMESTAMP(6) NOT NULL,
    updated_at       TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_oauth_identities_provider_user UNIQUE (provider, provider_user_id),
    CONSTRAINT uk_oauth_identities_user_provider UNIQUE (user_id, provider),
    CONSTRAINT fk_oauth_identities_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT ck_oauth_identities_provider CHECK (provider IN ('GOOGLE', 'KAKAO'))
);
CREATE INDEX idx_oauth_identities_user ON oauth_identities (user_id);

CREATE TABLE refresh_token (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email      VARCHAR(255) NOT NULL UNIQUE,
    token      VARCHAR(255) NOT NULL,
    expires_at TIMESTAMP(6) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL
);

-- ===================== 성향 설문 =====================
CREATE TABLE survey_questions (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code          VARCHAR(10) NOT NULL UNIQUE,
    dimension     VARCHAR(20) NOT NULL,
    content       VARCHAR(200) NOT NULL,
    is_reverse    BOOLEAN NOT NULL,
    display_order INTEGER NOT NULL,
    created_at    TIMESTAMP(6) NOT NULL,
    updated_at    TIMESTAMP(6) NOT NULL,
    CONSTRAINT ck_survey_questions_dimension CHECK (
        dimension IN ('ACTIVITY', 'FOOD', 'PACE', 'URBAN_NATURE', 'TIME_PREF')
    )
);

CREATE TABLE user_preferences (
    user_id      BIGINT PRIMARY KEY,
    activity     DOUBLE PRECISION NOT NULL,
    food         DOUBLE PRECISION NOT NULL,
    pace         DOUBLE PRECISION NOT NULL,
    urban_nature DOUBLE PRECISION NOT NULL,
    time_pref    DOUBLE PRECISION NOT NULL,
    created_at   TIMESTAMP(6) NOT NULL,
    updated_at   TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_user_preferences_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT ck_user_preferences_activity     CHECK (activity     BETWEEN 0.0 AND 1.0),
    CONSTRAINT ck_user_preferences_food         CHECK (food         BETWEEN 0.0 AND 1.0),
    CONSTRAINT ck_user_preferences_pace         CHECK (pace         BETWEEN 0.0 AND 1.0),
    CONSTRAINT ck_user_preferences_urban_nature CHECK (urban_nature BETWEEN 0.0 AND 1.0),
    CONSTRAINT ck_user_preferences_time_pref    CHECK (time_pref    BETWEEN 0.0 AND 1.0)
);

-- ===================== 그룹 =====================
CREATE TABLE groups (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title                    VARCHAR(30) NOT NULL,
    destination              VARCHAR(100) NOT NULL,
    start_date               DATE NOT NULL,
    end_date                 DATE NOT NULL,
    cover_image_key          VARCHAR(255),     -- 프리셋 식별자 또는 'CUSTOM'
    cover_object_key         VARCHAR(255),     -- 커스텀 커버 S3 key
    cover_image_content_type VARCHAR(100),
    invite_code              VARCHAR(6) NOT NULL UNIQUE,
    status                   VARCHAR(20) NOT NULL,
    deleted_at               TIMESTAMP(6),
    created_at               TIMESTAMP(6) NOT NULL,
    updated_at               TIMESTAMP(6) NOT NULL,
    CONSTRAINT ck_groups_date_range CHECK (start_date <= end_date),
    CONSTRAINT ck_groups_status CHECK (status IN ('PLANNING', 'IN_PROGRESS', 'COMPLETED', 'DELETED'))
);

CREATE TABLE group_members (
    id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id  BIGINT NOT NULL,
    user_id   BIGINT NOT NULL,
    role      VARCHAR(20) NOT NULL,
    joined_at TIMESTAMP(6) NOT NULL,
    left_at   TIMESTAMP(6),
    CONSTRAINT uk_group_members_group_user UNIQUE (group_id, user_id),
    CONSTRAINT fk_group_members_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_group_members_user FOREIGN KEY (user_id) REFERENCES users (id),
    CONSTRAINT ck_group_members_role CHECK (role IN ('OWNER', 'MEMBER'))
);
CREATE INDEX idx_group_members_active_user  ON group_members (user_id, left_at);
CREATE INDEX idx_group_members_active_group ON group_members (group_id, left_at);

CREATE TABLE group_photos (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id     BIGINT NOT NULL,
    uploaded_by  BIGINT NOT NULL,
    object_key   VARCHAR(255) NOT NULL,   -- S3 key (구버전 bytea → V21에서 전환)
    content_type VARCHAR(100) NOT NULL,
    created_at   TIMESTAMP(6) NOT NULL,
    updated_at   TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_group_photos_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_group_photos_uploader FOREIGN KEY (uploaded_by) REFERENCES users (id)
);
CREATE INDEX idx_group_photos_group ON group_photos (group_id);

-- ===================== 장소 =====================
CREATE TABLE places (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    google_place_id    VARCHAR(255) NOT NULL UNIQUE,
    name               VARCHAR(255) NOT NULL,
    address            VARCHAR(500),
    latitude           DOUBLE PRECISION NOT NULL,
    longitude          DOUBLE PRECISION NOT NULL,
    types              VARCHAR(500),
    price_level        VARCHAR(30),
    rating             DOUBLE PRECISION,
    rating_count       INTEGER,
    photo_name         VARCHAR(500),
    google_maps_uri    VARCHAR(500),
    phone_number       VARCHAR(50),
    opening_hours      VARCHAR(2000),
    website_uri        VARCHAR(500),
    details_fetched_at TIMESTAMP(6),
    created_at         TIMESTAMP(6) NOT NULL,
    updated_at         TIMESTAMP(6) NOT NULL
);

CREATE TABLE bookmarks (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id        BIGINT NOT NULL,
    place_id        BIGINT NOT NULL,
    created_by      BIGINT NOT NULL,
    category_tag    VARCHAR(30) NOT NULL,
    memo            VARCHAR(500),
    personal_rating INTEGER,
    created_at      TIMESTAMP(6) NOT NULL,
    updated_at      TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_bookmarks_group_place UNIQUE (group_id, place_id),
    CONSTRAINT fk_bookmarks_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_bookmarks_place FOREIGN KEY (place_id) REFERENCES places (id),
    CONSTRAINT fk_bookmarks_creator FOREIGN KEY (created_by) REFERENCES users (id)
);
CREATE INDEX idx_bookmarks_group ON bookmarks (group_id);

CREATE TABLE place_search_cache (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cache_key  VARCHAR(500) NOT NULL UNIQUE,
    result_json TEXT NOT NULL,
    expires_at TIMESTAMP(6) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL
);

-- ===================== 일정 / 이동 / 숙소 =====================
CREATE TABLE schedules (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id       BIGINT NOT NULL,
    place_id       BIGINT,                       -- nullable: 빈 일정(투표로 결정) 허용
    title          VARCHAR(100),                 -- 빈 일정 제목
    schedule_date  DATE NOT NULL,
    order_index    INTEGER NOT NULL,
    start_time     TIME(6) NOT NULL,
    end_time       TIME(6) NOT NULL,
    memo           VARCHAR(500),
    estimated_cost BIGINT,
    transport_mode VARCHAR(20),
    status         VARCHAR(20) NOT NULL,
    created_by     BIGINT NOT NULL,
    updated_by     BIGINT NOT NULL,
    created_at     TIMESTAMP(6) NOT NULL,
    updated_at     TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_schedules_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_schedules_place FOREIGN KEY (place_id) REFERENCES places (id),
    CONSTRAINT fk_schedules_creator FOREIGN KEY (created_by) REFERENCES users (id),
    CONSTRAINT fk_schedules_updater FOREIGN KEY (updated_by) REFERENCES users (id),
    CONSTRAINT ck_schedules_status CHECK (status IN ('PLANNED', 'VOTING', 'CANCELLED')),
    CONSTRAINT ck_schedules_transport_mode CHECK (transport_mode IS NULL OR transport_mode IN ('CAR', 'TRANSIT', 'WALK'))
);
CREATE INDEX idx_schedules_group_date_order ON schedules (group_id, schedule_date, order_index);

CREATE TABLE transport_legs (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cache_key        VARCHAR(200) NOT NULL UNIQUE,
    mode             VARCHAR(20) NOT NULL,
    available        BOOLEAN NOT NULL,
    duration_seconds INTEGER,
    distance_meters  INTEGER,
    toll             INTEGER,
    fuel_cost        INTEGER,
    taxi_fare        INTEGER,
    transit_fare     INTEGER,
    transfer_count   INTEGER,
    route_summary    VARCHAR(500),
    note             VARCHAR(200),
    expires_at       TIMESTAMP(6) NOT NULL,      -- 1시간 캐시
    created_at       TIMESTAMP(6) NOT NULL,
    CONSTRAINT ck_transport_legs_mode CHECK (mode IN ('CAR', 'TRANSIT', 'WALK'))
);

CREATE TABLE group_accommodations (
    id                         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id                   BIGINT NOT NULL,
    place_id                   BIGINT NOT NULL,
    created_by                 BIGINT NOT NULL,
    sigungu                    VARCHAR(100),
    status                     VARCHAR(20) NOT NULL,
    reservation_price          BIGINT,
    booking_photo_key          VARCHAR(255),      -- S3 key (V21 전환)
    booking_photo_content_type VARCHAR(100),
    stay_date                  DATE,              -- 체크인
    stay_end_date              DATE,              -- 체크아웃(여러 박)
    created_at                 TIMESTAMP(6) NOT NULL,
    updated_at                 TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_group_acc_group FOREIGN KEY (group_id) REFERENCES groups (id),
    CONSTRAINT fk_group_acc_place FOREIGN KEY (place_id) REFERENCES places (id),
    CONSTRAINT fk_group_acc_creator FOREIGN KEY (created_by) REFERENCES users (id),
    CONSTRAINT ck_group_acc_status CHECK (status IN ('SELECTED', 'BOOKED'))
);
CREATE INDEX idx_group_acc_group      ON group_accommodations (group_id);
CREATE INDEX idx_group_acc_group_date ON group_accommodations (group_id, stay_date);

